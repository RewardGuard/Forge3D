export const PART_BY_ID = Object.fromEntries(PARTS.map((p) => [p.id, p]));

// ---- world scale -----------------------------------------------------------
// Real parts are tiny (mm). We scale them up so the 3D workspace isn't "micro"
// to work with, while keeping their *relative* real-world proportions.
export const SCENE_SCALE = 12; // mm -> scene units (a 40mm part ≈ 0.48 units)

const _longestUnits = PARTS.map(
  (p) => (Math.max(p.size.w, p.size.h, p.size.d) / 1000) * SCENE_SCALE
);
export const AVG_PART_UNIT = _longestUnits.reduce((a, b) => a + b, 0) / _longestUnits.length;

// Default size for primitives & imported models: 2.5× the average circuit part,
// so a fresh shape reads as clearly bigger than the parts but not gigantic.
export const DEFAULT_SHAPE_UNIT = +(AVG_PART_UNIT * 2.5).toFixed(3);
